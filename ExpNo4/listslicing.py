# Original list
my_list = [10, 20, 30, 40, 50, 60, 70]

print("Original list:", my_list)

# Basic slicing
print("my_list[1:5]:", my_list[1:5])

# From beginning
print("my_list[:4]:", my_list[:4])

# Till end
print("my_list[3:]:", my_list[3:])

# Step slicing
print("my_list[0:7:2]:", my_list[0:7:2])

# Reverse list
print("my_list[::-1]:", my_list[::-1])